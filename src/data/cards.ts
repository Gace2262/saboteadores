type CardDraft = Omit<Card, "nameKey" | "effectKey" | "flavorKey" | "impactKey" | "collectible" | "unlockCondition" | "packSources" | "duplicateValue" | "flavorQuote"> &
  Partial<Pick<Card, "nameKey" | "effectKey" | "flavorKey" | "impactKey" | "collectible" | "unlockCondition" | "packSources" | "duplicateValue" | "flavorQuote" | "cursedEffect">>;

const keyStem = (id: string) => (id === "el-juez" ? "judge" : id.replace(/-([a-z0-9])/g, (_, letter: string) => letter.toUpperCase()));

const withCollection = (card: CardDraft): Card => ({
  ...card,
  nameKey: card.nameKey ?? `cards.${keyStem(card.id)}.name`,
  effectKey: card.effectKey ?? `cards.${keyStem(card.id)}.effect`,
  flavorKey: card.flavorKey ?? `cards.${keyStem(card.id)}.flavor`,
  impactKey: card.impactKey ?? `cards.${keyStem(card.id)}.impact`,
  collectible: card.collectible ?? true,
  unlockCondition: card.unlockCondition ?? "Disponible en sobres y recompensas de campana.",
  packSources:
    card.packSources ??
    (card.rarity === "legendaria"
      ? ["tribunal", "catarsis"]
      : card.rarity === "epica"
        ? ["crisis-premium", "catarsis"]
        : ["intrusivos"]),
  duplicateValue: card.duplicateValue ?? (card.rarity === "legendaria" ? 80 : card.rarity === "epica" ? 40 : card.rarity === "rara" ? 20 : 8),
  flavorQuote: card.flavorQuote ?? card.darkHumorText,
});

